package com.hns.iusp.admin.bean;

public class SchoolTermBean {

	private String schoolYear;
	private String schoolTerm;
	private String schoolOpeanDate;
	private String schoolCloseDate;
	
	public String getSchoolYear() {
		return schoolYear;
	}
	public void setSchoolYear(String schoolYear) {
		this.schoolYear = schoolYear;
	}
	public String getSchoolTerm() {
		return schoolTerm;
	}
	public void setSchoolTerm(String schoolTerm) {
		this.schoolTerm = schoolTerm;
	}
	public String getSchoolOpeanDate() {
		return schoolOpeanDate;
	}
	public void setSchoolOpeanDate(String schoolOpeanDate) {
		this.schoolOpeanDate = schoolOpeanDate;
	}
	public String getSchoolCloseDate() {
		return schoolCloseDate;
	}
	public void setSchoolCloseDate(String schoolCloseDate) {
		this.schoolCloseDate = schoolCloseDate;
	}
	
	
}
